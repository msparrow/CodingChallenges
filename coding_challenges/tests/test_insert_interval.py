import pytest
from solutions.solution_44 import insert

@pytest.mark.parametrize("intervals, new_interval, expected", [
    ([[1, 3], [6, 9]], [2, 5], [[1, 5], [6, 9]]),
    ([[1, 2], [3, 5], [6, 7], [8, 10], [12, 16]], [4, 8], [[1, 2], [3, 10], [12, 16]]),
    ([], [5, 7], [[5, 7]]),
    ([[1, 5]], [2, 3], [[1, 5]]),
    ([[1, 5]], [6, 8], [[1, 5], [6, 8]]),
    ([[6, 8]], [1, 5], [[1, 5], [6, 8]]),
    ([[1, 5]], [0, 0], [[0, 0], [1, 5]]),
    ([[1, 5]], [0, 6], [[0, 6]]),
    ([[1, 5]], [1, 5], [[1, 5]]),
    ([[1, 5]], [0, 1], [[0, 5]]),
    ([[1, 5]], [5, 6], [[1, 6]]),
    ([[1, 2], [3, 4]], [2, 3], [[1, 4]]),
    ([[1, 2], [4, 5]], [3, 3], [[1, 2], [3, 3], [4, 5]]),
    ([[1, 10]], [2, 9], [[1, 10]]),
    ([[2, 9]], [1, 10], [[1, 10]]),
    ([[1, 2], [3, 5], [6, 7], [8, 10], [12, 16]], [4, 8], [[1, 2], [3, 10], [12, 16]]),
    ([[1, 5]], [2, 7], [[1, 7]]),
    ([[2, 7]], [1, 5], [[1, 7]]),
    ([[1, 5]], [0, 3], [[0, 5]])
])
def test_insert(intervals, new_interval, expected):
    assert insert(intervals, new_interval) == expected